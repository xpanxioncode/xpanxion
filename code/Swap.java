public class Swap {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;

        System.out.println("A is "+ a);
        System.out.println("B is "+b);

        //swaping..
	int temp = a;
	b = temp;
	a = b;


        System.out.println("A is now "+ a);
        System.out.println("B is now "+b);
		System.out.println("Hi Raghu!");

    }
}
